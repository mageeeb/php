        <p>Athènes est la capitale et la plus grande ville de la Grèce. En 2011, elle compte 664 046 habitants intra-muros sur une superficie de 39 km2. Son aire urbaine, le Grand Athènes, qui comprend notamment le port du Pirée, en compte plus de 4 millions. Berceau de la civilisation occidentale et dotée d'un riche passé, la ville est aujourd'hui le cœur politique, économique et culturel de la République hellénique, dont elle accueille la plupart des institutions, comme le Parlement, la Cour de cassation et le siège du Gouvernement.</p>

        <p> Athènes est l'une des plus anciennes villes au monde, avec une présence humaine attestée dès le Néolithique. Fondée vers 800 av. J.-C. autour de la colline de l'Acropole — par le héros Thésée, selon la légende —, la cité domine la Grèce au cours du Ier millénaire av. J.‑C. Elle connaît son âge d'or au ve siècle av. J.-C., sous la domination du stratège Périclès : principale puissance militaire de Grèce, à la tête d'une vaste alliance de cités, elle est également le cœur culturel de la Méditerranée. La démocratie athénienne connaît une vie intellectuelle importante, rassemblant des philosophes antiques (Socrate, Platon, Aristote), des auteurs de théâtre (Eschyle, Sophocle, Euripide, Aristophane) et l'historien Thucydide. De nombreux témoignages de cette période faste ont été conservés, comme le Parthénon, l'Agora (qui comprend notamment le temple d'Héphaïstos), l'Olympéion, le théâtre de Dionysos ou encore le Stade panathénaïque.</p>

        <p> Intégrée au royaume de Macédoine, puis à l'Empire byzantin — période durant laquelle fut bâti le monastère de Daphni —, Athènes est conquise par l'Empire ottoman en 1456 et reste sous sa domination jusqu'en 1822, année de l'indépendance de la Grèce. Elle en devient la capitale et connaît une importante croissance urbaine. Occupée lors des deux guerres mondiales, la ville est ravagée par les destructions de la guerre civile grecque (1946–1949). Durant la seconde moitié du xxe siècle, Athènes devient le cœur économique et universitaire d'une Grèce en plein développement, dont les symboles sont les grandes avenues commerçantes telles que l'avenue Kifissias ou l'avenue Vasilissis Sofias. Frappée de plein fouet par la crise grecque depuis 2009, la ville a perdu plusieurs dizaines de milliers d'habitants et fait face à d'importantes difficultés économiques.</p>

        <p> La ville s'étend sur la plaine d'Attique, sur les rives de la mer Égée. Elle est le chef-lieu du district régional d'Athènes-Centre et la capitale de la périphérie d'Attique, mais aussi celle du diocèse décentralisé du même nom. Le quartier le plus ancien, Pláka, est situé en contrebas de l'Acropole et comprend notamment la place Monastiráki. Kolonáki est le quartier huppé, à proximité de la place Syntagma. Enfin, Exárcheia est le quartier alternatif et branché de la ville, foyer de l'anarchisme en Grèce et de la contestation populaire depuis la crise. Grâce à son climat méditerranéen et ses nombreux musées, comme le musée de l'Acropole ou le musée national archéologique, Athènes est l'une des principales destinations touristiques d'Europe. En 1985, elle a été désignée comme capitale européenne de la culture et en 2018, comme capitale européenne de l'innovation. Elle a également accueilli les Jeux olympiques d'été en 1896 et en 2004.</p>


